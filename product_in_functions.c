//program to find product of two numbers
#include<stdio.h>
/*function declaration */int product();
int main()
{
    /*local variable  definition */
    int answer;
    
   /*calling a function to get product value*/
    answer = product();
    printf("the product of the two numbers is:%d\n",answer);
    return 0;
}
/*function returning the product of two numbers*/
int product()
{
    /*local variable definition*/
    int num1 =2,num2 =3;
    return num1*num2;
}
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
